    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        int selection = contactsTable.getSelectedRow();
        if (selection != -1) {
            int modelIndex = contactsTable.convertRowIndexToModel(selection);
            new FrmEditarContacto(ctbl, list, list.getLista().get(modelIndex)).setVisible(true);
        } else {
            editBtn.setEnabled(false);
            removeBtn.setEnabled(false);
        }
    }//GEN-LAST:event_editBtnActionPerformed

    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
        int selection = contactsTable.getSelectedRow();
        if (selection != -1) {
            int modelIndex = contactsTable.convertRowIndexToModel(selection);
            Contacto c = list.getLista().get(modelIndex);
            if (confirmar("Â¿Desea eliminar al contacto "
                    + c.getNombre() + " " + c.getApellido() + "?")) {
                list.getLista().remove(modelIndex);
                list.guardar();
                ctbl.updateModelo(list);
            }
        } else {
            editBtn.setEnabled(false);
            removeBtn.setEnabled(false);
        }
    }//GEN-LAST:event_removeBtnActionPerformed

    private void searchTxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchTxtKeyTyped
        /*
            SoluciÃ³n implementada gracias a la valiosa ayuda
            de mi compaÃ±ero Jonathan Mostacero
         */
        trs = new TableRowSorter(ctbl.getModelo());
        searchTxt.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent e) {
                try {
                    trs.setRowFilter(RowFilter.regexFilter(searchTxt.getText(), 0));
                } catch (PatternSyntaxException err) {
                    JOptionPane.showMessageDialog(null,
                            "TÃ©rmino de bÃºsqueda invÃ¡lido.\nIntÃ©ntelo nuevamente.",
                            "BÃºsqueda invÃ¡lida", 2);
                    searchTxt.setText(null);
                }
            }
        });
        contactsTable.setRowSorter(trs);
    }//GEN-LAST:event_searchTxtKeyTyped

